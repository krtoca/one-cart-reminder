
const fs = require("fs");
const path = require("path");

const file = path.join(process.cwd(), "app", "services", "cart-capture.server.ts");

if (!fs.existsSync(file)) {
  console.error("Missing file:", file);
  process.exit(1);
}

let src = fs.readFileSync(file, "utf8");

// Add helper functions after normalizeEmail.
if (!src.includes("function cartLineItemsSignature")) {
  src = src.replace(
    /function normalizeEmail\(value: unknown\) \{\s*return String\(value \|\| ""\)\.trim\(\)\.toLowerCase\(\);\s*\}/,
    `function normalizeEmail(value: unknown) {
  return String(value || "").trim().toLowerCase();
}

function cartLineItemsSignature(value: unknown) {
  if (!Array.isArray(value)) return "";

  return value
    .map((item: any) => {
      const variantId = String(item?.variantId || item?.variant_id || item?.id || "").replace(/\\D/g, "");
      const quantity = Number(item?.quantity || 0);
      return variantId && quantity > 0 ? \`\${variantId}:\${quantity}\` : "";
    })
    .filter(Boolean)
    .sort()
    .join("|");
}

async function shouldSkipRecentlyClearedCart(params: {
  shop: string;
  email: string;
  customerId?: string | null;
  cartToken?: string | null;
  lineItems: any[];
}) {
  const recentCutoff = new Date(Date.now() - 30 * 60 * 1000);
  const identityOr: any[] = [{ customerEmail: params.email }];

  if (params.customerId) {
    identityOr.push({ customerId: String(params.customerId) });
  }

  const recentCleared = await prisma.customerCart.findFirst({
    where: {
      shop: params.shop,
      orderedAt: { not: null },
      orderedAt: { gte: recentCutoff },
      OR: identityOr,
    },
    orderBy: { orderedAt: "desc" },
  });

  if (!recentCleared) return false;

  if (params.cartToken && recentCleared.cartToken && params.cartToken === recentCleared.cartToken) {
    return true;
  }

  const incomingSignature = cartLineItemsSignature(params.lineItems);
  const clearedSignature = cartLineItemsSignature(recentCleared.lineItems);

  return Boolean(incomingSignature && clearedSignature && incomingSignature === clearedSignature);
}`
  );
}

// Fix duplicate orderedAt where if generated by helper replacement accidentally.
// Prisma object cannot have duplicate orderedAt keys. Ensure final block uses AND.
src = src.replace(
  /where:\s*\{\s*shop: params\.shop,\s*orderedAt: \{ not: null \},\s*orderedAt: \{ gte: recentCutoff \},\s*OR: identityOr,\s*\}/,
  `where: {
      shop: params.shop,
      orderedAt: { gte: recentCutoff },
      OR: identityOr,
    }`
);

// Insert skip check after itemCount empty check, before existing find/create.
if (!src.includes('reason: "recently_cleared_cart"')) {
  src = src.replace(
    /if \(itemCount <= 0\) \{[\s\S]*?return \{ ok: true, skipped: true, cleared: true, clearedCount, reason: "empty_cart" \};\s*\}/,
    (match) => `${match}

  const skipRecentlyCleared = await shouldSkipRecentlyClearedCart({
    shop,
    email,
    customerId: payload.customerId ? String(payload.customerId) : null,
    cartToken: payload.cartToken ? String(payload.cartToken) : null,
    lineItems,
  });

  if (skipRecentlyCleared) {
    return { ok: true, skipped: true, reason: "recently_cleared_cart" };
  }`
  );
}

fs.writeFileSync(file, src, "utf8");
console.log("Patched recapture prevention after clear/order:", file);
